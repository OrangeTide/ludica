/* CGA I/O handlers */
static uint8_t cga_read(lilpc_t *pc, uint16_t port)
{
	video_t *vid = &pc->video;

	switch (port) {
	case 0x3D1: case 0x3D3: case 0x3D5: case 0x3D7:
		/* CRTC data register (odd ports mirror 3D5) */
		if (vid->crtc.index < 18)
			return vid->crtc.reg[vid->crtc.index];
		return 0;

	case 0x3D8: return vid->mode_ctrl;
	case 0x3D9: return vid->color_sel;

	case 0x3DA:
	{
		/*
		 * CGA status register:
		 * Bit 0: display in horizontal retrace (toggles rapidly)
		 * Bit 1: light pen trigger (not emulated)
		 * Bit 3: display in vertical retrace
		 *
		 * Catch up video timing to current CPU cycle.
		 */
		video_tick(vid, pc, pc->cpu.cycles);

		uint8_t status = 0;
		/* CGA has 262 scanlines total, 200 active */
		int active_lines = 200;

		if (vid->scanline >= active_lines)
			status |= 0x09; /* vretrace + display enable */
		else {
			/* simulate hsync - toggle bit 0 based on cycle count */
			/* CGA horizontal: ~76 char clocks = 608 pixels = ~382 CPU cycles at 6MHz */
			uint32_t hpos = (uint32_t)(pc->cpu.cycles % 382);
			if (hpos > 320) /* ~84% of line is active */
				status |= 0x01;
		}

		return status;
	}
	}
	return 0xFF;
}

static void cga_write(lilpc_t *pc, uint16_t port, uint8_t val)
{
	video_t *vid = &pc->video;

	switch (port) {
	case 0x3D0: case 0x3D2: case 0x3D4: case 0x3D6:
		/* CRTC index register (even ports) */
		vid->crtc.index = val & 0x1F;
		break;

	case 0x3D1: case 0x3D3: case 0x3D5: case 0x3D7:
		/* CRTC data register */
		if (vid->crtc.index < 18)
			vid->crtc.reg[vid->crtc.index] = val;
		break;

	case 0x3D8:
		vid->mode_ctrl = val;
		break;

	case 0x3D9:
		vid->color_sel = val;
		break;
	}
}

/* Hercules I/O handlers */
static uint8_t herc_read(lilpc_t *pc, uint16_t port)
{
	video_t *vid = &pc->video;

	switch (port) {
	case 0x3B5:
		if (vid->crtc.index < 18)
			return vid->crtc.reg[vid->crtc.index];
		return 0;

	case 0x3BA:
	{
		/* Hercules status register */
		video_tick(vid, pc, pc->cpu.cycles);

		uint8_t status = 0;
		if (vid->scanline >= 348)
			status |= 0x80; /* vertical retrace */

		/* bit 0: hsync */
		uint32_t hpos = (uint32_t)(pc->cpu.cycles % 500);
		if (hpos > 400)
			status |= 0x01;

		return status;
	}
	}
	return 0xFF;
}

static void herc_write(lilpc_t *pc, uint16_t port, uint8_t val)
{
	video_t *vid = &pc->video;

	switch (port) {
	case 0x3B4:
		vid->crtc.index = val & 0x1F;
		break;

	case 0x3B5:
		if (vid->crtc.index < 18)
			vid->crtc.reg[vid->crtc.index] = val;
		break;

	case 0x3B8:
		vid->herc_mode = val;
		break;

	case 0x3BF:
		vid->herc_config = val;
		break;
	}
}

void video_init(video_t *vid, lilpc_t *pc, bool hercules)
{
	memset(vid, 0, sizeof(*vid));
	vid->hercules = hercules;

	if (hercules) {
		/* MDA/Hercules CRTC defaults (720x350 text) */
		static const uint8_t mda_crtc[18] = {
			97, 80, 82, 15, 25, 6, 25, 25,
			2, 13, 11, 12, 0, 0, 0, 0, 0, 0
		};
		memcpy(vid->crtc.reg, mda_crtc, 18);
		vid->render_w = 720;
		vid->render_h = 350;
		bus_register_io(&pc->bus, 0x3B0, 16, herc_read, herc_write);
	} else {
		/* CGA CRTC defaults (80x25 text, 640x200) */
		static const uint8_t cga_crtc[18] = {
			113, 80, 90, 10, 31, 6, 25, 28,
			2, 7, 6, 7, 0, 0, 0, 0, 0, 0
		};
		memcpy(vid->crtc.reg, cga_crtc, 18);
		vid->render_w = 640;
		vid->render_h = 200;
		vid->mode_ctrl = 0x29; /* 80-col text, enable display */
		bus_register_io(&pc->bus, 0x3D0, 16, cga_read, cga_write);
	}
}

/*
 * Render CGA text mode (40x25 or 80x25)
 */
static void render_cga_text(video_t *vid, bus_t *bus)
{
	int cols = (vid->mode_ctrl & 0x01) ? 80 : 40;
	int char_w = (cols == 80) ? 8 : 16; /* 40-col doubles pixel width */
	int char_h = 8;
	int rows = 25;

	vid->render_w = cols * char_w;
	vid->render_h = rows * char_h;

	/* cursor position from CRTC */
	uint16_t cursor_pos = ((uint16_t)vid->crtc.reg[14] << 8) | vid->crtc.reg[15];
	uint8_t cursor_start = vid->crtc.reg[10] & 0x1F;
	uint8_t cursor_end = vid->crtc.reg[11] & 0x1F;
	bool cursor_enabled = !(vid->crtc.reg[10] & 0x20);

	/* start address from CRTC */
	uint16_t start_addr = ((uint16_t)vid->crtc.reg[12] << 8) | vid->crtc.reg[13];

	for (int row = 0; row < rows; row++) {
		for (int col = 0; col < cols; col++) {
			uint16_t offset = ((start_addr + row * cols + col) * 2) & 0x3FFF;
			uint32_t addr = CGA_VRAM_BASE + offset;
			uint8_t ch = bus_read8(bus, addr);
			uint8_t attr = bus_read8(bus, addr + 1);

			uint32_t fg = cga_palette[attr & 0x0F];
			uint32_t bg = cga_palette[(attr >> 4) & 0x0F];

			/* blink mode: if mode_ctrl bit 5, bit 7 of attr = blink */
			if ((vid->mode_ctrl & 0x20) && (attr & 0x80)) {
				/* blink: toggle fg/bg based on frame counter */
				/* use scanline counter as crude blink timer */
				bg = cga_palette[(attr >> 4) & 0x07];
			}

			const uint8_t *glyph = &cga_font_8x8[ch * 8];

			for (int y = 0; y < char_h; y++) {
				uint8_t bits = glyph[y];
				for (int x = 0; x < 8; x++) {
					uint32_t color = (bits & (0x80 >> x)) ? fg : bg;

					if (cols == 40) {
						/* double-width pixels */
						int px = col * 16 + x * 2;
						int py = row * 8 + y;
						vid->pixels[py * vid->render_w + px] = color;
						vid->pixels[py * vid->render_w + px + 1] = color;
					} else {
						int px = col * 8 + x;
						int py = row * 8 + y;
						vid->pixels[py * vid->render_w + px] = color;
					}
				}
			}

			/* draw cursor */
			if (cursor_enabled &&
			    (start_addr + row * cols + col) == cursor_pos) {
				for (int y = cursor_start; y <= cursor_end && y < char_h; y++) {
					for (int x = 0; x < 8; x++) {
						if (cols == 40) {
							int px = col * 16 + x * 2;
							int py = row * 8 + y;
							vid->pixels[py * vid->render_w + px] = fg;
							vid->pixels[py * vid->render_w + px + 1] = fg;
						} else {
							int px = col * 8 + x;
							int py = row * 8 + y;
							vid->pixels[py * vid->render_w + px] = fg;
						}
					}
				}
			}
		}
	}
}

/*
 * Render CGA 320x200 4-color graphics mode
 */
static void render_cga_320(video_t *vid, bus_t *bus)
{
	vid->render_w = 320;
	vid->render_h = 200;

	/* select palette */
	int pal_idx = (vid->color_sel & 0x20) ? 1 : 0;
	bool bright = (vid->color_sel & 0x10) != 0;
	int bg_color = vid->color_sel & 0x0F;

	int pal[4];
	pal[0] = bg_color;
	for (int i = 1; i < 4; i++) {
		pal[i] = cga_gfx_pal[pal_idx][i];
		if (bright)
			pal[i] += 8;
	}

	for (int y = 0; y < 200; y++) {
		/* CGA interleaves even/odd scanlines at different offsets */
		uint32_t row_addr = CGA_VRAM_BASE;
		if (y & 1)
			row_addr += 0x2000;
		row_addr += (y >> 1) * 80;

		for (int x = 0; x < 320; x += 4) {
			uint8_t byte = bus_read8(bus, row_addr + (x >> 2));
			for (int px = 0; px < 4; px++) {
				int cidx = (byte >> (6 - px * 2)) & 3;
				vid->pixels[y * 320 + x + px] = cga_palette[pal[cidx]];
			}
		}
	}
}

/*
 * Render CGA 640x200 2-color graphics mode
 */
static void render_cga_640(video_t *vid, bus_t *bus)
{
	vid->render_w = 640;
	vid->render_h = 200;

	uint32_t fg = cga_palette[vid->color_sel & 0x0F];
	uint32_t bg = cga_palette[0];

	for (int y = 0; y < 200; y++) {
		uint32_t row_addr = CGA_VRAM_BASE;
		if (y & 1)
			row_addr += 0x2000;
		row_addr += (y >> 1) * 80;

		for (int x = 0; x < 640; x += 8) {
			uint8_t byte = bus_read8(bus, row_addr + (x >> 3));
			for (int px = 0; px < 8; px++) {
				uint32_t color = (byte & (0x80 >> px)) ? fg : bg;
				vid->pixels[y * 640 + x + px] = color;
			}
		}
	}
}

/*
 * Render Hercules text mode (80x25)
 */
static void render_herc_text(video_t *vid, bus_t *bus)
{
	vid->render_w = 720;
	vid->render_h = 350;

	/* Hercules text is MDA-compatible: 80x25, 9x14 cells */
	/* but we use 8x8 font for simplicity, scaled to fill */
	int cols = 80;
	int rows = 25;
	int char_w = 9;
	int char_h = 14;

	uint16_t cursor_pos = ((uint16_t)vid->crtc.reg[14] << 8) | vid->crtc.reg[15];
	uint8_t cursor_start = vid->crtc.reg[10] & 0x1F;
	uint8_t cursor_end = vid->crtc.reg[11] & 0x1F;
	bool cursor_enabled = !(vid->crtc.reg[10] & 0x20);

	uint32_t fg_color = herc_palette[1];
	uint32_t bg_color = herc_palette[0];

	memset(vid->pixels, 0, sizeof(vid->pixels));

	for (int row = 0; row < rows; row++) {
		for (int col = 0; col < cols; col++) {
			uint32_t addr = HERC_VRAM_BASE + (row * cols + col) * 2;
			uint8_t ch = bus_read8(bus, addr);
			uint8_t attr = bus_read8(bus, addr + 1);

			/* MDA attribute decoding:
			 * 00 = invisible, 01 = underline, 07 = normal, 0F = bright
			 * 70 = reverse, 87/8F = blink
			 */
			uint32_t fg, bg;
			if (attr == 0x00 || attr == 0x08 || attr == 0x80) {
				fg = bg_color;
				bg = bg_color;
			} else if ((attr & 0x77) == 0x70) {
				fg = bg_color;
				bg = fg_color;
			} else {
				fg = fg_color;
				bg = bg_color;
			}

			const uint8_t *glyph = &cga_font_8x8[ch * 8];

			for (int y = 0; y < char_h; y++) {
				/* scale 8-row font to 14 rows */
				int font_y = y * 8 / 14;
				uint8_t bits = glyph[font_y];

				/* underline: attr bit 0 set, bits 2:0 = 001 */
				if ((attr & 0x07) == 0x01 && y == char_h - 1)
					bits = 0xFF;

				for (int x = 0; x < 8; x++) {
					uint32_t color = (bits & (0x80 >> x)) ? fg : bg;
					int px = col * char_w + x;
					int py = row * char_h + y;
					if (px < VIDEO_MAX_W && py < VIDEO_MAX_H)
						vid->pixels[py * vid->render_w + px] = color;
				}
				/* 9th pixel column: repeat column 8 for box-drawing (C0-DF) */
				{
					int px = col * char_w + 8;
					int py = row * char_h + y;
					uint32_t color = bg;
					if (ch >= 0xC0 && ch <= 0xDF)
						color = (bits & 0x01) ? fg : bg;
					if (px < VIDEO_MAX_W && py < VIDEO_MAX_H)
						vid->pixels[py * vid->render_w + px] = color;
				}
			}

			/* cursor */
			if (cursor_enabled && (row * cols + col) == cursor_pos) {
				for (int y = cursor_start; y <= cursor_end && y < char_h; y++) {
					for (int x = 0; x < char_w; x++) {
						int px = col * char_w + x;
						int py = row * char_h + y;
						if (px < VIDEO_MAX_W && py < VIDEO_MAX_H)
							vid->pixels[py * vid->render_w + px] = fg;
					}
				}
			}
		}
	}
}

/*
 * Render Hercules 720x348 graphics mode
 */
static void render_herc_gfx(video_t *vid, bus_t *bus)
{
	vid->render_w = 720;
	vid->render_h = 348;

	/* which page? bit 7 of herc_mode selects page */
	uint32_t page_base = HERC_VRAM_BASE;
	if (vid->herc_mode & 0x80)
		page_base += 0x8000;

	for (int y = 0; y < 348; y++) {
		/* Hercules interleaves 4 banks */
		uint32_t row_addr = page_base + (y & 3) * 0x2000 + (y >> 2) * 90;

		for (int x = 0; x < 720; x += 8) {
			uint8_t byte = bus_read8(bus, row_addr + (x >> 3));
			for (int px = 0; px < 8; px++) {
				uint32_t color = (byte & (0x80 >> px))
					? herc_palette[1] : herc_palette[0];
				vid->pixels[y * 720 + x + px] = color;
			}
		}
	}
}

/*
 * CGA scanline doubling: expand native 200-line output to 400 lines
 * with blank (black) odd scanlines, simulating NTSC non-interlaced
 * CGA output where only one field is sent repeatedly.
 *
 * For 320-wide modes, also doubles horizontally to produce 640x400.
 * Works in-place by processing rows bottom-to-top.
 */
static void cga_scanline_double(video_t *vid)
{
	int src_w = vid->render_w;
	int src_h = vid->render_h;
	int dst_w = (src_w <= 320) ? 640 : src_w;

	/* expand bottom-to-top so we don't overwrite unread source data */
	for (int y = src_h - 1; y >= 0; y--) {
		uint32_t *src_row = &vid->pixels[y * src_w];
		uint32_t *dst_even = &vid->pixels[(y * 2) * dst_w];
		uint32_t *dst_odd = &vid->pixels[(y * 2 + 1) * dst_w];

		/* blank the odd scanline */
		memset(dst_odd, 0, dst_w * sizeof(uint32_t));

		/* copy/expand the even scanline */
		if (src_w <= 320) {
			/* double horizontally: 320 → 640 */
			for (int x = src_w - 1; x >= 0; x--) {
				dst_even[x * 2] = src_row[x];
				dst_even[x * 2 + 1] = src_row[x];
			}
		} else if (dst_even != src_row) {
			memmove(dst_even, src_row, src_w * sizeof(uint32_t));
		}
	}

	vid->render_w = dst_w;
	vid->render_h = src_h * 2;
}

void video_render(video_t *vid, lilpc_t *pc)
{
	if (vid->hercules) {
		if (vid->herc_mode & 0x02) /* graphics bit */
			render_herc_gfx(vid, &pc->bus);
		else
			render_herc_text(vid, &pc->bus);
	} else {
		/* CGA mode decoding from mode_ctrl (port 3D8h):
		 * Bit 0: 80-column text
		 * Bit 1: graphics mode
		 * Bit 2: disable color burst (B&W)
		 * Bit 3: enable video output
		 * Bit 4: 640x200 graphics
		 * Bit 5: blink enable
		 */
		if (!(vid->mode_ctrl & 0x08)) {
			/* display disabled - black screen */
			vid->render_w = 640;
			vid->render_h = 400;
			memset(vid->pixels, 0, 640 * 400 * sizeof(uint32_t));
			return;
		}

		if (vid->mode_ctrl & 0x02) {
			/* graphics mode */
			if (vid->mode_ctrl & 0x10)
				render_cga_640(vid, &pc->bus);
			else
				render_cga_320(vid, &pc->bus);
		} else {
			render_cga_text(vid, &pc->bus);
		}

		/* apply NTSC-style scanline doubling */
		cga_scanline_double(vid);
	}
}

void video_tick(video_t *vid, lilpc_t *pc __attribute__((unused)), uint64_t cpu_cycles)
{
	/*
	 * Update scanline counter based on elapsed CPU cycles.
	 * CGA: 262 scanlines per frame, ~382 CPU cycles per scanline at 6 MHz
	 * Hercules: 370 scanlines per frame, ~500 CPU cycles per scanline
	 *
	 * Use absolute cycle count modulo frame length to avoid
	 * accumulated rounding errors from frequent small updates.
	 */
	(void)cpu_cycles;

	int cycles_per_line;
	int total_lines;
	if (vid->hercules) {
		cycles_per_line = 500;
		total_lines = 370;
	} else {
		cycles_per_line = 382;
		total_lines = 262;
	}

	int frame_cycles = cycles_per_line * total_lines;
	int pos = (int)(cpu_cycles % (uint64_t)frame_cycles);
	vid->scanline = pos / cycles_per_line;
}

void video_get_size(video_t *vid, int *w, int *h)
{
	*w = vid->render_w;
	*h = vid->render_h;
}
