/*
 * font.c — Bitmap font rendering.
 *
 * Supports fixed-width bitmap fonts stored as texture atlases.
 * Includes a built-in 8x8 font covering ASCII 32-127 (font8x8_basic).
 * Text drawing uses the sprite batch, so call between sprite_begin/end.
 */

#include "initgl_gfx.h"
#include "initgl_font.h"
#include <string.h>

#define MAX_FONTS 8

typedef struct {
	int used;
	initgl_texture_t tex;
	int chars_wide;
	int glyph_w, glyph_h;
	int first_char;
} font_slot_t;

static font_slot_t slots[MAX_FONTS];

static int
alloc_slot(void)
{
	int i;
	for (i = 0; i < MAX_FONTS; i++)
		if (!slots[i].used)
			return i;
	return -1;
}

static font_slot_t *
get_slot(initgl_font_t font)
{
	if (font.id == 0 || font.id > MAX_FONTS)
		return NULL;
	font_slot_t *s = &slots[font.id - 1];
	return s->used ? s : NULL;
}

initgl_font_t
initgl_make_font(initgl_texture_t tex, int chars_wide,
                 int glyph_w, int glyph_h, int first_char)
{
	initgl_font_t out = {0};
	int idx = alloc_slot();
	font_slot_t *s;

	if (idx < 0)
		return out;

	s = &slots[idx];
	s->used = 1;
	s->tex = tex;
	s->chars_wide = chars_wide;
	s->glyph_w = glyph_w;
	s->glyph_h = glyph_h;
	s->first_char = first_char;

	out.id = (unsigned)(idx + 1);
	return out;
}

