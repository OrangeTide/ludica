#define DEFAULT_FONT_COLS 16
#define DEFAULT_FONT_FIRST 32
#define DEFAULT_FONT_GLYPHS 96
#define DEFAULT_FONT_GW 8
#define DEFAULT_FONT_GH 8

initgl_font_t
initgl_make_default_font(void)
{
	int tex_w = DEFAULT_FONT_COLS * DEFAULT_FONT_GW;  /* 128 */
	int tex_h = ((DEFAULT_FONT_GLYPHS + DEFAULT_FONT_COLS - 1) / DEFAULT_FONT_COLS) * DEFAULT_FONT_GH;  /* 48 */
	unsigned char pixels[128 * 48 * 4];
	int i, row, col, px, py;
	initgl_texture_t tex;

	memset(pixels, 0, sizeof(pixels));

	for (i = 0; i < DEFAULT_FONT_GLYPHS; i++) {
		col = i % DEFAULT_FONT_COLS;
		row = i / DEFAULT_FONT_COLS;
		for (py = 0; py < 8; py++) {
			unsigned char bits = font8x8_data[i][py];
			for (px = 0; px < 8; px++) {
				if (bits & (0x80 >> px)) {
					int x = col * 8 + px;
					int y = row * 8 + py;
					int off = (y * tex_w + x) * 4;
					pixels[off + 0] = 255;
					pixels[off + 1] = 255;
					pixels[off + 2] = 255;
					pixels[off + 3] = 255;
				}
			}
		}
	}

	tex = initgl_make_texture(&(initgl_texture_desc_t){
		.width = tex_w,
		.height = tex_h,
		.format = INITGL_PIXFMT_RGBA8,
		.min_filter = INITGL_FILTER_NEAREST,
		.mag_filter = INITGL_FILTER_NEAREST,
		.data = pixels,
	});

	return initgl_make_font(tex, DEFAULT_FONT_COLS, DEFAULT_FONT_GW, DEFAULT_FONT_GH, DEFAULT_FONT_FIRST);
}

void
initgl_destroy_font(initgl_font_t font)
{
	font_slot_t *s = get_slot(font);
	if (!s) return;
	initgl_destroy_texture(s->tex);
	memset(s, 0, sizeof(*s));
}

int
initgl_text_width(initgl_font_t font, const char *text)
{
	font_slot_t *s = get_slot(font);
	if (!s || !text) return 0;
	return (int)strlen(text) * s->glyph_w;
}

void
initgl_draw_text(initgl_font_t font, float x, float y,
                 float scale, const char *text)
{
	font_slot_t *s = get_slot(font);
	float gw, gh, cx;
	const char *p;

	if (!s || !text) return;

	gw = (float)s->glyph_w * scale;
	gh = (float)s->glyph_h * scale;
	cx = x;

	for (p = text; *p; p++) {
		int ch = (unsigned char)*p;
		int idx, col, row;
		float sx, sy;

		if (ch < s->first_char || ch >= s->first_char + (s->chars_wide * 128)) {
			cx += gw;
			continue;
		}

		idx = ch - s->first_char;
		col = idx % s->chars_wide;
		row = idx / s->chars_wide;
		sx = (float)(col * s->glyph_w);
		sy = (float)(row * s->glyph_h);

		initgl_sprite_draw(s->tex, cx, y, gw, gh,
		                   sx, sy, (float)s->glyph_w, (float)s->glyph_h);
		cx += gw;
	}
}

void
initgl_draw_text_centered(initgl_font_t font, float x, float y,
                          float scale, const char *text)
{
	font_slot_t *s;
	float w;

	if (!text) return;
	s = get_slot(font);
	if (!s) return;

	w = (float)strlen(text) * (float)s->glyph_w * scale;
	initgl_draw_text(font, x - w / 2.0f, y, scale, text);
}

void
initgl_draw_text_wrapped(initgl_font_t font, float x, float y,
                         float scale, float max_width,
                         float line_spacing, const char *text)
{
	font_slot_t *s = get_slot(font);
	float gw, line_y;
	const char *p, *word_start, *line_start;
	int line_len, word_len;
	char line_buf[256];

	if (!s || !text) return;

	gw = (float)s->glyph_w * scale;
	line_y = y;
	line_start = text;
	line_len = 0;

	p = text;
	while (*p) {
		/* find next word */
		word_start = p;
		word_len = 0;

		/* skip spaces */
		while (*p == ' ') {
			p++;
			word_len++;
		}
		/* measure word */
		while (*p && *p != ' ') {
			p++;
			word_len++;
		}

		/* would this word overflow? */
		if (line_len > 0 && (float)(line_len + word_len) * gw > max_width) {
			/* flush current line */
			if (line_len > 0 && (unsigned)line_len < sizeof(line_buf)) {
				memcpy(line_buf, line_start, line_len);
				line_buf[line_len] = '\0';
				initgl_draw_text(font, x, line_y, scale, line_buf);
			}
			line_y += line_spacing;

			/* skip leading space on new line */
			if (*word_start == ' ') {
				word_start++;
				word_len--;
			}
			line_start = word_start;
			line_len = word_len;
		} else {
			line_len += word_len;
		}
	}

	/* flush remaining */
	if (line_len > 0 && (unsigned)line_len < sizeof(line_buf)) {
		memcpy(line_buf, line_start, line_len);
		line_buf[line_len] = '\0';
		initgl_draw_text(font, x, line_y, scale, line_buf);
	}
}
